template <typename T>
void print_vector(const vector<T> &v)
{
    for (const T &x : v)
        cout << x << ' ';
    cout << '\n';
}

template <typename T>
void input_vector(vector<T> &v)
{
    for (T &x : v)
        cin >> x;
}

void solve()
{
    int w, b;
    cin >> w >> b;
    int row = 0;
    int k = 1;
    int mx = max(w, b);
    int mn = min(w, b);

    while (k <= mn)
    {
        mn -= k;
        k++;
        row++;
    }

    while (k <= mx)
    {
        mx -= k;
        k++;
        row++;
    }

    cout << row << endl;
}

int main()
{
    fast_io();

    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}
